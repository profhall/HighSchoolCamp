def age_calculator(current, birth):
    return current - birth


print(age_calculator(2019, 1924))
print(age_calculator(2019, 1832))
print(age_calculator(2019, 2005))


def avg_numbers(x, y, z):
    return (x + y + z) / 3


print(avg_numbers(10, 24, 65))
print(avg_numbers(3, 4, 5))
print(avg_numbers(2, 80, 90))
